제1장 총칙

제 1 조 (목적)
이 약관은 (주)써드브레인(이하 “회사”라 합니다)의 인터넷 서비스 코디탑텐www.coditop10.com(이하 “서비스”라 합니다)을 이용하는 고객(이하 “샵회원”이라 합니다)에 대하여 회사가 제공하는 서비스의 가입조건 및 이용에 관한 제반 사항과 기타 필요한 사항을 구체적으로 규정함을 목적으로 합니다.

제 2 조 (이용약관의 효력 및 변경)
1. 이 약관은 코디탑텐 www.coditop10.com 서비스에서 온라인으로 공지함으로써 효력을 발생하며, 합리적인 사유가 발생할 경우 관련 법령에 위배되지 않는 범위 안에서 개정될 수 있습니다. 개정된 약관은 온라인에서 공지함으로써 효력이 발생하며, 샵회원의 권리 또는 의무 등 중요한 규정의 개정은 사전에 공지합니다.
